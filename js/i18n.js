let currentLang = "en";

function t(key) {
  return TRANSLATIONS[currentLang][key] ?? TRANSLATIONS.en[key] ?? key;
}

function applyLanguage(lang) {
  currentLang = lang === "zh" ? "zh" : "en";
  localStorage.setItem("apexhut-lang", currentLang);

  document.documentElement.lang = currentLang === "zh" ? "zh-Hant" : "en";
  document.title = t("page.title");

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", t("meta.description"));

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const active = btn.dataset.lang === currentLang;
    btn.classList.toggle("is-active", active);
    btn.setAttribute("aria-pressed", String(active));
  });

  document.body.classList.toggle("lang-zh", currentLang === "zh");

  if (typeof window.onLanguageChange === "function") {
    window.onLanguageChange(currentLang);
  }
}

function initLanguage() {
  const saved = localStorage.getItem("apexhut-lang");
  const browserZh = navigator.language?.toLowerCase().startsWith("zh");
  applyLanguage(saved || (browserZh ? "zh" : "en"));

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
}
